#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - sum
 * @a: number 1
 * @b: number 2
 * Return: a b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - rest
 * @a: number 1
 * @b: number 2
 * Return: a b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul
 * @a: number 1
 * @b: number 2
 * Return: a b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div
 * @a: number 1
 * @b: number 2
 * Return: a b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * @a: number 1
 * @b: number 2
 * Return: a b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
